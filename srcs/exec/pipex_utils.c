#include "minishell.h"

char	*back_to_positive(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == -1)
			s[i] = 36;
		if (s[i] < 0)
			s[i] *= -1;
		i++;
	}
	return (s);
}
